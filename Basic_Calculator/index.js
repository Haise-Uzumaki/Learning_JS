function sumAdd(a , b){
    result = a + b;
    console.log("Addition of a and b is: " + result);
}

function sumSub(a , b){
    result = a - b;
    console.log("Subtraction of a and b is: " + result);
}

function sumMul(a , b){
    result = a * b;
    console.log("Multiplication of a and b is: " + result);
}

function sumDiv(a , b){
    result = a / b;
    console.log("Division of a and b is: " + result);
}


// call the function for result 
//eg.

sumAdd( 4, 6);
sumSub( 4, 6);
sumMul( 4, 6);
sumDiv( 4, 6);